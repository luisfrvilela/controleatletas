package controleatleta;


public class AtributosMentais {
    
    private String sensoDeQuadra;//Excelente , Otimo , Regular , Ruim , Pessimo
    private String atitudeDeEquipe;//Excelente , Otimo , Regular , Ruim , Pessimo
    private String competitividade;//Alta, Media, Baixa
    private String lideranca;//Alta, Media, Baixa
    private String impactoDaPressao;//Impacto da "pressao" no aleta: Alta, Media, Baixa

    
    public String getSensoDeQuadra() {
        return sensoDeQuadra;
    }

    public void setSensoDeQuadra(String sensoDeQuadra) {
        this.sensoDeQuadra = sensoDeQuadra;
    }

    public String getAtitudeDeEquipe() {
        return atitudeDeEquipe;
    }

    
    public void setAtitudeDeEquipe(String atitudeDeEquipe) {
        this.atitudeDeEquipe = atitudeDeEquipe;
    }

    
    public String getCompetitividade() {
        return competitividade;
    }

    public void setCompetitividade(String competitividade) {
        this.competitividade = competitividade;
    }

    public String getLideranca() {
        return lideranca;
    }

    public void setLideranca(String lideranca) {
        this.lideranca = lideranca;
    }

    
    public String getImpactoDaPressao() {
        return impactoDaPressao;
    }

    public void setImpactoDaPressao(String impactoDaPressao) {
        this.impactoDaPressao = impactoDaPressao;
    }

   
   
    
}
